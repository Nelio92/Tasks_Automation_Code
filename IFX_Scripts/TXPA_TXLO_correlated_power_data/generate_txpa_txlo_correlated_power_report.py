def _extract_stdf_members_from_tar(archive_path: Path, destination: Path) -> list[Path]:
    extracted: list[Path] = []
    destination.mkdir(parents=True, exist_ok=True)
    with tarfile.open(archive_path, mode="r:gz") as archive:
        for member in archive.getmembers():
            if not member.isfile():
                continue
            member_name = Path(member.name).name
            if not _has_any_suffix(Path(member_name), (".std", ".stdf", ".std.gz", ".stdf.gz", ".std.xz", ".stdf.xz", ".std.bz2", ".stdf.bz2")):
                continue

            target_path = destination / member_name
            if target_path.exists():
                stem = target_path.stem
                suffix = "".join(target_path.suffixes)
                counter = 1
                while True:
                    candidate = destination / f"{stem}__{counter}{suffix}"
                    if not candidate.exists():
                        target_path = candidate
                        break
                    counter += 1

            source_handle = archive.extractfile(member)
            if source_handle is None:
                continue

            with source_handle, target_path.open("wb") as target_handle:
                target_handle.write(source_handle.read())

            extracted.append(target_path)
    return extracted


def _progress(percent: float, message: str) -> None:
    clamped = max(0.0, min(100.0, float(percent)))
    print(f"[{clamped:6.2f}%] {message}", flush=True)


