"""\
TXPA/TXLO temperature-based CDF report from flat CSV, STDF, or .std.tar.gz inputs.

Goal
----
Build merged insertion-level CDFs for TXLO/TXPA correlated-power style measurements.
Each insertion/temperature curve merges all wafers available for that insertion.

Supported inputs
----------------
- Flat CSV generated by ``stdf_to_flat_csv.py``
- Raw STDF files: ``.std``, ``.stdf``, ``.std.gz``, ``.stdf.gz``, ``.std.xz``, ...
- Tarballs that contain STDF files: ``.std.tar.gz`` / ``.stdf.tar.gz``

Default behavior
----------------
- No extra pre-correlation is applied.
- Existing raw limits from the flat CSV are used.

Optional behavior
-----------------
- If ``ENABLE_PRE_CORRELATION`` is set to ``True``, the script applies the same
  correlation-factor logic used by the previous flow and, when available, uses
  the correlation-limit spreadsheets.

Notes
-----
- No CLI by design; edit the CONFIG block.
- Outputs are written under ``INPUT_FOLDER / "Outputs"``.
- Filename metadata extraction is heuristic because multiple naming schemes are
  present in the repository.
"""

