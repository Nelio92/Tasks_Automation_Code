def _plot_temperature_cdf(
    *,
    out_path: Path,
    meta: TestMeta,
    insertion_infos: list[SourceFileInfo],
    series_by_insertion: dict[str, np.ndarray],
    limits_by_insertion: dict[str, tuple[float | None, float | None, str]],
    extrema_by_key: dict[tuple, dict[str, dict]],
) -> None:
    insertion_type = insertion_infos[0].insertion_type
    order = _INSERTION_ORDER_FE if insertion_type == "FE" else _INSERTION_ORDER_BE

    n_cols = len(order)
    fig, axes = plt.subplots(1, n_cols, figsize=(5.6 * n_cols, 6.2), sharey=True)
    if n_cols == 1:
        axes = [axes]

    title_bits = [f"T{meta.test_number}", meta.test_name]
    if meta.kind == "TXLO" and meta.lo_idac is not None:
        title_bits.append(f"LO IDAC {meta.lo_idac}")
    if meta.kind == "TXPA" and meta.lut_value is not None:
        title_bits.append(f"LUT {meta.lut_value}")
    if meta.frequency_ghz is not None:
        title_bits.append(f"{meta.frequency_ghz} GHz")
    if meta.pa_channel:
        title_bits.append(meta.pa_channel)
    fig.suptitle(" | ".join(title_bits))

    unit_for_axis = "dBm"

    for ax, insertion in zip(axes, order, strict=False):
        ax.grid(True, alpha=0.25)
        ax.set_xlabel("Power")
        if insertion == order[0]:
            ax.set_ylabel("CDF")

        values = series_by_insertion.get(insertion)
        if values is None or values.size == 0:
            ax.set_title(f"{insertion} / {_TEMP_BY_INS[insertion]}°C\nno data")
            continue

        x, y = _cdf_xy(values)
        if x.size == 0:
            ax.set_title(f"{insertion} / {_TEMP_BY_INS[insertion]}°C\nno valid data")
            continue

        source_rows = [row for row in insertion_infos if row.insertion == insertion]
        wafers = sorted({row.wafer for row in source_rows if row.wafer})
        lots = sorted({row.lot for row in source_rows if row.lot})

        extrema_tag = ""
        is_min = False
        is_max = False
        line_color = INSERTION_COLORS[insertion]
        if meta.kind == "TXLO" and meta.lo_idac == int(TARGET_TXLO_LO_IDAC) and meta.frequency_ghz in EXTREMA_FREQS:
            extrema_key = ("TXLO", int(meta.lo_idac))
            extrema_info = extrema_by_key.get(extrema_key, {})
            is_min = extrema_info.get("min", {}).get("test_number") == int(meta.test_number) and extrema_info.get("min", {}).get("insertion") == insertion
            is_max = extrema_info.get("max", {}).get("test_number") == int(meta.test_number) and extrema_info.get("max", {}).get("insertion") == insertion
        elif meta.kind == "TXPA" and meta.lut_value == int(TARGET_TXPA_LUT) and meta.frequency_ghz in EXTREMA_FREQS:
            extrema_key = ("TXPA", insertion_type, (meta.pa_channel or "ALL").strip().upper() or "ALL")
            extrema_info = extrema_by_key.get(extrema_key, {})
            is_min = extrema_info.get("min", {}).get("test_number") == int(meta.test_number) and extrema_info.get("min", {}).get("insertion") == insertion
            is_max = extrema_info.get("max", {}).get("test_number") == int(meta.test_number) and extrema_info.get("max", {}).get("insertion") == insertion

        if is_min:
            extrema_tag = " [MIN median]"
            line_color = HIGHLIGHT_MIN_COLOR
        elif is_max:
            extrema_tag = " [MAX median]"
            line_color = HIGHLIGHT_MAX_COLOR

        label = f"n={values.size}, wafers={max(len(wafers), 1)}{extrema_tag}"
        ax.scatter(
            x,
            y,
            color=line_color,
            s=20 if (is_min or is_max) else 12,
            marker="o",
            edgecolors=line_color,
            facecolors="none" if not (is_min or is_max) else line_color,
            linewidths=1.0 if (is_min or is_max) else 0.8,
            label=label,
        )

        low, high, unit = limits_by_insertion.get(insertion, (None, None, ""))
        unit_for_axis = unit or unit_for_axis
        limit_handles_added = False
        if low is not None:
            ax.axvline(low, color="red", linestyle="-", linewidth=1.2, alpha=0.9)
            limit_handles_added = True
        if high is not None:
            ax.axvline(high, color="red", linestyle="-", linewidth=1.2, alpha=0.9)
            limit_handles_added = True

        stats_text = (
            f"mean={np.mean(values):.3f}\n"
            f"med={_median(values):.3f}\n"
            f"std={np.std(values):.3f}\n"
            f"wafers={','.join(wafers) if wafers else '-'}\n"
            f"lots={','.join(lots) if lots else '-'}"
        )
        ax.text(
            0.02,
            0.98,
            stats_text,
            transform=ax.transAxes,
            ha="left",
            va="top",
            fontsize=7,
            family="monospace",
            bbox={"boxstyle": "round,pad=0.25", "facecolor": "white", "alpha": 0.78, "edgecolor": "none"},
        )
        ax.set_title(f"{insertion} / {_TEMP_BY_INS[insertion]}°C{extrema_tag}")
        if limit_handles_added:
            ax.plot([], [], color="red", linestyle="-", linewidth=1.2, label=f"Limits [{low:.3f}, {high:.3f}] {unit_for_axis}")
        else:
            ax.plot([], [], color="red", linestyle="-", linewidth=1.2, label="Limits [missing]")
        ax.legend(loc="lower right", fontsize=8, framealpha=0.9)

    for ax in axes:
        ax.set_xlabel(f"Power [{unit_for_axis or 'dBm'}]")

    fig.tight_layout(rect=[0, 0.02, 1, 0.93])
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path, dpi=PLOT_DPI)
    plt.close(fig)


# -----------------------------------------------------------------------------
# Main
# -----------------------------------------------------------------------------


def main() -> int:
    input_folder = Path(INPUT_FOLDER)
    if not input_folder.exists():
        raise SystemExit(f"Input folder not found: {input_folder}")

    _progress(0, f"Starting TXPA/TXLO report generation from {input_folder}")

    run_tag = datetime.now().strftime("%Y%m%d_%H%M%S")
    output_root = input_folder / "Outputs" / f"txpa_txlo_power_cdf_{run_tag}"
    artifacts_dir = output_root / "Artifacts"
    plots_dir = output_root / "cdf_plots"
    cache_dir = input_folder / "Outputs" / "_txpa_txlo_cache"
    artifacts_dir.mkdir(parents=True, exist_ok=True)
    plots_dir.mkdir(parents=True, exist_ok=True)
    cache_dir.mkdir(parents=True, exist_ok=True)
    _progress(5, f"Created output folders under {output_root}")

    with tempfile.TemporaryDirectory(prefix="txpa_txlo_work_") as temp_work_dir:
        temp_work_path = Path(temp_work_dir)
        conversion_work_dir = temp_work_path / "conversion_work"

        _progress(10, "Preparing input files")
        input_files = _prepare_input_files(
            input_folder,
            work_dir=conversion_work_dir,
            cache_dir=cache_dir,
            progress_callback=lambda fraction, message: _progress(
                10 + (20 * fraction),
                message,
            ),
        )
        if not input_files:
            raise SystemExit(f"No supported input files found in: {input_folder}")

        _progress(32, f"Prepared {len(input_files)} input file(s) for analysis")

        txlo_factor: dict[tuple[int, int, int, int], float] = {}
        txlo_limits: dict[tuple[int, int, int], dict[str, tuple[float, float, str]]] = {}
        txpa_factor_fe: dict[tuple[int, int, int, str], float] = {}
        txpa_factor_be: dict[tuple[int, int, int, str], float] = {}
        txpa_limits_fe: dict[tuple[int, int, int, str], dict[str, tuple[float, float, str]]] = {}
        txpa_limits_be: dict[tuple[int, int, int, str], dict[str, tuple[float, float, str]]] = {}

        if ENABLE_PRE_CORRELATION:
            _progress(36, "Loading correlation factors and limits")
            required = [TXLO_CORR_XLSX_FE, TXPA_CORR_XLSX_FE, TXPA_CORR_XLSX_BE]
            missing = [str(path) for path in required if not path.exists()]
            if missing:
                raise FileNotFoundError("Correlation files are missing:\n- " + "\n- ".join(missing))
            txlo_factor, txlo_limits = _load_txlo_factors_and_limits()
            txpa_factor_fe, txpa_limits_fe = _load_txpa_factors_and_limits_fe()
            txpa_factor_be, txpa_limits_be = _load_txpa_factors_and_limits_be()
        else:
            _progress(36, "Skipping correlation-factor loading")

        source_rows: list[dict] = []
        test_name_by_num: dict[int, str] = {}
        raw_limits_by_ins_test: dict[tuple[str, int], list[tuple[float | None, float | None, str]]] = {}
        arrays_by_ins_test: dict[tuple[str, int], list[np.ndarray]] = {}
        meta_by_test: dict[int, TestMeta] = {}
        outlier_rows: list[dict] = []
        missing_factor_rows: list[dict] = []
        missing_limit_rows: list[dict] = []

        target_tests: set[int] = set()
        limits_cache_by_file: dict[Path, dict[int, tuple[float | None, float | None, str]]] = {}

        _progress(40, "Scanning prepared CSV metadata for target tests")
        total_inputs = max(len(input_files), 1)
        for index, info in enumerate(input_files, start=1):
            _progress(40 + (10 * index / total_inputs), f"Scanning metadata {index}/{total_inputs}: {_input_display_name(info)}")
            if info.direct_power_data is not None:
                name_map = dict(info.direct_power_data.name_map)
                limits_map = dict(info.direct_power_data.limits_map)
            elif info.csv_path is not None:
                name_map, limits_map = _read_flat_csv_metadata(info.csv_path)
                limits_cache_by_file[info.csv_path] = limits_map
            else:
                continue
            for test_number, test_name in name_map.items():
                meta = _parse_test_meta(test_number, test_name)
                if not _is_target_power_test(meta):
                    continue
                if not _is_selected_power_setting(meta):
                    continue
                raw_unit = limits_map.get(int(test_number), (None, None, ""))[2]
                if not _is_dbm_unit(raw_unit):
                    continue
                if TEST_NUMBER_FILTER and int(test_number) not in TEST_NUMBER_FILTER:
                    continue
                target_tests.add(int(test_number))
                test_name_by_num.setdefault(int(test_number), test_name)
                if meta is not None:
                    meta_by_test.setdefault(int(test_number), meta)

        if not target_tests:
            raise SystemExit("No TXLO/TXPA correlated-power style tests were found in the selected inputs.")

        _progress(52, f"Found {len(target_tests)} target test(s)")

        for index, info in enumerate(input_files, start=1):
            _progress(52 + (23 * index / total_inputs), f"Reading chip data {index}/{total_inputs}: {_input_display_name(info)}")
            direct_power_data = info.direct_power_data
            chip_matrix = None
            if direct_power_data is None:
                if info.csv_path is None:
                    continue
                chip_matrix, read_info = _read_flat_csv_chip_matrix(info.csv_path, tests_needed=target_tests)
                if read_info.get("error"):
                    continue
                if chip_matrix.empty:
                    continue

            source_rows.append(
                {
                    "source_file": str(info.source_path),
                    "csv_file": "" if info.csv_path is None else str(info.csv_path),
                    "source_kind": info.source_kind,
                    "lot": info.lot,
                    "wafer": info.wafer,
                    "insertion": info.insertion,
                    "insertion_type": info.insertion_type,
                    "temperature_c": info.temperature_c,
                    "n_chips": int(direct_power_data.n_chips if direct_power_data is not None else chip_matrix.shape[0]),
                }
            )

            if direct_power_data is not None:
                limits_map = dict(direct_power_data.limits_map)
                present_tests = sorted(set(direct_power_data.arrays_by_test).intersection(target_tests))
            else:
                limits_map = limits_cache_by_file.get(info.csv_path, {})
                present_tests = sorted(set(chip_matrix.columns).intersection(target_tests))
            for test_number in present_tests:
                if direct_power_data is not None:
                    arr = np.asarray(direct_power_data.arrays_by_test.get(int(test_number), np.array([], dtype=float)), dtype=float)
                else:
                    arr = pd.to_numeric(chip_matrix[int(test_number)], errors="coerce").dropna().to_numpy(dtype=float)
                if arr.size == 0:
                    continue

                filtered, removed = _filter_outliers_mad(arr, mad_multiplier=MAD_MULTIPLIER)
                if removed:
                    outlier_rows.append(
                        {
                            "source_file": str(info.source_path),
                            "csv_file": str(info.csv_path),
                            "insertion": info.insertion,
                            "test_number": int(test_number),
                            "n_before": int(arr.size),
                            "n_after": int(filtered.size),
                            "n_removed": int(removed),
                            "mad_multiplier": float(MAD_MULTIPLIER),
                        }
                    )
                if filtered.size == 0:
                    continue

                meta = meta_by_test.get(int(test_number))
                if meta is None:
                    continue

                analysis_values = filtered
                applied_factor = None
                limits_for_plot = limits_map.get(int(test_number), (None, None, ""))
                if not _is_dbm_unit(limits_for_plot[2]):
                    continue

                if ENABLE_PRE_CORRELATION:
                    if meta.kind == "TXLO" and meta.frequency_ghz is not None and meta.lo_idac is not None:
                        applied_factor = txlo_factor.get((int(test_number), int(meta.frequency_ghz), int(info.temperature_c), int(meta.lo_idac)))
                        corr_limit_map = txlo_limits.get((int(test_number), int(meta.frequency_ghz), int(meta.lo_idac)), {})
                        if info.insertion in corr_limit_map:
                            limits_for_plot = corr_limit_map[info.insertion]
                    elif meta.kind == "TXPA" and meta.frequency_ghz is not None and meta.lut_value is not None:
                        factor_lookup = txpa_factor_fe if info.insertion_type == "FE" else txpa_factor_be
                        limit_lookup = txpa_limits_fe if info.insertion_type == "FE" else txpa_limits_be
                        applied_factor = _lookup_txpa_factor(
                            factor_lookup,
                            lut=int(meta.lut_value),
                            freq=int(meta.frequency_ghz),
                            temp_c=int(info.temperature_c),
                            pa_channel=meta.pa_channel,
                        )
                        corr_limit_map = _lookup_txpa_limits(
                            limit_lookup,
                            test_number=int(test_number),
                            lut=int(meta.lut_value),
                            freq=int(meta.frequency_ghz),
                            pa_channel=meta.pa_channel,
                        )
                        if info.insertion in corr_limit_map:
                            limits_for_plot = corr_limit_map[info.insertion]

                    if applied_factor is not None:
                        analysis_values = filtered + float(applied_factor)
                    else:
                        missing_factor_rows.append(
                            {
                                "source_file": str(info.source_path),
                                "insertion": info.insertion,
                                "test_number": int(test_number),
                                "test_name": meta.test_name,
                                "kind": meta.kind,
                                "frequency_ghz": meta.frequency_ghz,
                                "lut": meta.lut_value,
                                "lo_idac": meta.lo_idac,
                                "pa_channel": meta.pa_channel,
                            }
                        )

                    if limits_for_plot[0] is None and limits_for_plot[1] is None:
                        missing_limit_rows.append(
                            {
                                "source_file": str(info.source_path),
                                "insertion": info.insertion,
                                "test_number": int(test_number),
                                "test_name": meta.test_name,
                                "kind": meta.kind,
                                "frequency_ghz": meta.frequency_ghz,
                                "lut": meta.lut_value,
                                "lo_idac": meta.lo_idac,
                                "pa_channel": meta.pa_channel,
                            }
                        )

                arrays_by_ins_test.setdefault((info.insertion, int(test_number)), []).append(analysis_values)
                raw_limits_by_ins_test.setdefault((info.insertion, int(test_number)), []).append(limits_for_plot)

        _progress(78, "Merging insertion-level series")
        merged_series_by_ins_test: dict[tuple[str, int], np.ndarray] = {}
        for key, parts in arrays_by_ins_test.items():
            if parts:
                merged_series_by_ins_test[key] = np.concatenate(parts)

        _progress(82, "Computing extrema summaries")
        extrema_candidates: dict[tuple, list[dict]] = {}
        for (insertion, test_number), series in merged_series_by_ins_test.items():
            meta = meta_by_test.get(int(test_number))
            if meta is None or not _is_selected_power_setting(meta):
                continue
            if meta.frequency_ghz not in EXTREMA_FREQS:
                continue

            row = {
                "kind": meta.kind,
                "test_number": int(test_number),
                "test_name": meta.test_name,
                "insertion": insertion,
                "insertion_type": _INS_TYPE_BY_INS[insertion],
                "temp_c": _TEMP_BY_INS[insertion],
                "freq_ghz": meta.frequency_ghz,
                "median": float(_median(series)),
                "lut": meta.lut_value,
                "lo_idac": meta.lo_idac,
                "pa_channel": (meta.pa_channel or "ALL").strip().upper() or "ALL",
            }

            if meta.kind == "TXLO" and meta.lo_idac == int(TARGET_TXLO_LO_IDAC):
                extrema_candidates.setdefault(("TXLO", int(meta.lo_idac)), []).append(row)
            elif meta.kind == "TXPA" and meta.lut_value == int(TARGET_TXPA_LUT):
                extrema_candidates.setdefault(("TXPA", _INS_TYPE_BY_INS[insertion], row["pa_channel"]), []).append(row)

        extrema_by_key: dict[tuple, dict[str, dict]] = {}
        extrema_summary_rows: list[dict] = []
        for key, rows in extrema_candidates.items():
            valid_rows = [row for row in rows if np.isfinite(row["median"])]
            if not valid_rows:
                continue
            min_row = min(valid_rows, key=lambda row: float(row["median"]))
            max_row = max(valid_rows, key=lambda row: float(row["median"]))
            extrema_by_key[key] = {"min": min_row, "max": max_row}
            extrema_summary_rows.append(
                {
                    "key": str(key),
                    "kind": min_row["kind"],
                    "insertion_type": min_row["insertion_type"],
                    "lut": min_row["lut"],
                    "lo_idac": min_row["lo_idac"],
                    "pa_channel": min_row["pa_channel"],
                    "min_test_number": min_row["test_number"],
                    "min_freq_ghz": min_row["freq_ghz"],
                    "min_insertion": min_row["insertion"],
                    "min_temp_c": min_row["temp_c"],
                    "min_median": min_row["median"],
                    "max_test_number": max_row["test_number"],
                    "max_freq_ghz": max_row["freq_ghz"],
                    "max_insertion": max_row["insertion"],
                    "max_temp_c": max_row["temp_c"],
                    "max_median": max_row["median"],
                }
            )

        _progress(86, "Generating plots and aggregate summaries")
        summary_rows: list[dict] = []
        test_numbers = sorted({test_number for (_, test_number) in arrays_by_ins_test})
        total_tests = max(len(test_numbers), 1)
        for plot_index, test_number in enumerate(test_numbers, start=1):
            _progress(86 + (10 * plot_index / total_tests), f"Plotting test {plot_index}/{total_tests}: T{int(test_number)}")
            meta = meta_by_test.get(int(test_number))
            if meta is None:
                continue
            if not _is_selected_power_setting(meta):
                continue

            has_fe = any((ins, test_number) in arrays_by_ins_test for ins in _INSERTION_ORDER_FE)
            insertion_type = "FE" if has_fe else "BE"
            insertion_order = _INSERTION_ORDER_FE if insertion_type == "FE" else _INSERTION_ORDER_BE
            relevant_sources = [row for row in input_files if row.insertion_type == insertion_type]

            series_by_insertion: dict[str, np.ndarray] = {}
            limits_by_insertion: dict[str, tuple[float | None, float | None, str]] = {}
            for insertion in insertion_order:
                arrays = arrays_by_ins_test.get((insertion, int(test_number)), [])
                if arrays:
                    series = np.concatenate(arrays)
                    series_by_insertion[insertion] = series
                    limit_rows = raw_limits_by_ins_test.get((insertion, int(test_number)), [])
                    lows = [row[0] for row in limit_rows if row[0] is not None]
                    highs = [row[1] for row in limit_rows if row[1] is not None]
                    unit = next((row[2] for row in limit_rows if row[2]), "dBm")
                    limits_by_insertion[insertion] = (
                        min(lows) if lows else None,
                        max(highs) if highs else None,
                        unit,
                    )
                    source_subset = [row for row in relevant_sources if row.insertion == insertion]
                    summary_rows.append(
                        {
                            "test_number": int(test_number),
                            "test_name": meta.test_name,
                            "kind": meta.kind,
                            "frequency_ghz": meta.frequency_ghz,
                            "lut": meta.lut_value,
                            "lo_idac": meta.lo_idac,
                            "pa_channel": meta.pa_channel,
                            "insertion": insertion,
                            "temperature_c": _TEMP_BY_INS[insertion],
                            "n_values": int(series.size),
                            "mean": float(np.mean(series)),
                            "median": float(_median(series)),
                            "std": float(np.std(series)),
                            "low_limit": limits_by_insertion[insertion][0],
                            "high_limit": limits_by_insertion[insertion][1],
                            "unit": limits_by_insertion[insertion][2],
                            "lots": "|".join(sorted({row.lot for row in source_subset if row.lot})),
                            "wafers": "|".join(sorted({row.wafer for row in source_subset if row.wafer})),
                            "correlation_applied": bool(ENABLE_PRE_CORRELATION),
                        }
                    )

            if not series_by_insertion:
                continue

            out_name = f"{meta.kind}__T{int(test_number)}"
            if meta.frequency_ghz is not None:
                out_name += f"__{meta.frequency_ghz}GHz"
            if meta.kind == "TXLO" and meta.lo_idac is not None:
                out_name += f"__PowCo{meta.lo_idac}"
            if meta.kind == "TXPA" and meta.lut_value is not None:
                out_name += f"__FwLu{meta.lut_value}"
            if meta.pa_channel:
                out_name += f"__{meta.pa_channel}"

            _plot_temperature_cdf(
                out_path=plots_dir / insertion_type / f"{out_name}.png",
                meta=meta,
                insertion_infos=relevant_sources,
                series_by_insertion=series_by_insertion,
                limits_by_insertion=limits_by_insertion,
                extrema_by_key=extrema_by_key,
            )

        _progress(97, "Writing report outputs")
        pd.DataFrame(source_rows).to_csv(output_root / "processed_input_files.csv", index=False)
        pd.DataFrame(summary_rows).to_csv(output_root / "aggregated_series_summary.csv", index=False)
        if outlier_rows:
            pd.DataFrame(outlier_rows).to_csv(output_root / "outliers_removed_mad.csv", index=False)
        if missing_factor_rows:
            pd.DataFrame(missing_factor_rows).drop_duplicates().to_csv(output_root / "missing_correlation_factors.csv", index=False)
        if missing_limit_rows:
            pd.DataFrame(missing_limit_rows).drop_duplicates().to_csv(output_root / "missing_correlation_limits.csv", index=False)
        if extrema_summary_rows:
            pd.DataFrame(extrema_summary_rows).to_csv(output_root / "extrema_median_summary.csv", index=False)

    _progress(100, f"Done. Output: {output_root}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
