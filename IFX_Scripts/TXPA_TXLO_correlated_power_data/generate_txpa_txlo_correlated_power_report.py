def _direct_cache_signature(path: Path, *, extra_key: str = "") -> dict[str, Any]:
    stat = path.stat()
    return {
        "path": str(path.resolve()),
        "size": int(stat.st_size),
        "mtime_ns": int(getattr(stat, "st_mtime_ns", int(stat.st_mtime * 1_000_000_000))),
        "extra_key": extra_key,
        "target_txlo_lo_idac": int(TARGET_TXLO_LO_IDAC),
        "target_txpa_lut": int(TARGET_TXPA_LUT),
        "test_filter": None if TEST_NUMBER_FILTER is None else sorted(int(value) for value in TEST_NUMBER_FILTER),
    }


def _direct_cache_path(cache_dir: Path, path: Path, *, extra_key: str = "") -> Path:
    base_name = _strip_known_suffixes(path.name)
    token = hashlib.sha1(f"{path.resolve()}::{extra_key}".encode("utf-8", errors="ignore")).hexdigest()[:12]
    return cache_dir / f"{base_name}__{token}.pkl"


def _load_cached_direct_power_data(cache_path: Path, *, signature: dict[str, Any]) -> DirectPowerData | None:
    if not cache_path.exists():
        return None
    try:
        with cache_path.open("rb") as handle:
            payload = pickle.load(handle)
    except Exception:
        return None

    if not isinstance(payload, dict) or payload.get("signature") != signature:
        return None

    data = payload.get("data")
    if not isinstance(data, DirectPowerData):
        return None
    return data


def _save_cached_direct_power_data(cache_path: Path, *, signature: dict[str, Any], data: DirectPowerData) -> None:
    cache_path.parent.mkdir(parents=True, exist_ok=True)
    payload = {"signature": signature, "data": data}
    with cache_path.open("wb") as handle:
        pickle.dump(payload, handle, protocol=pickle.HIGHEST_PROTOCOL)


