def _has_any_suffix(path: Path, suffixes: tuple[str, ...]) -> bool:
    name = path.name.lower()
    return any(name.endswith(sfx) for sfx in suffixes)


def _strip_known_suffixes(name: str) -> str:
    lower = name.lower()
    for suffix in sorted(_TAR_STDF_SUFFIXES + _DIRECT_STDF_SUFFIXES + (".csv",), key=len, reverse=True):
        if lower.endswith(suffix):
            return name[: -len(suffix)]
    return Path(name).stem


def _insertion_from_filename(path: Path) -> InsertionInfo | None:
    name_upper = path.name.upper()
    if "S11P" in name_upper:
        return InsertionInfo("S1", "FE", 135)
    if "S21P" in name_upper:
        return InsertionInfo("S2", "FE", -40)
    if "S31P" in name_upper:
        return InsertionInfo("S3", "FE", 25)
    if "B11P" in name_upper:
        return InsertionInfo("B1", "BE", 135)
    if "B21P" in name_upper:
        return InsertionInfo("B2", "BE", 25)
    return None


def _parse_filename_metadata(path: Path, insertion: InsertionInfo) -> tuple[str | None, str | None]:
    _ = insertion
    stem = _strip_known_suffixes(path.name)
    tokens = [tok for tok in stem.split("_") if tok]
    insertion_idx = next((i for i, tok in enumerate(tokens) if _INSERTION_TOKEN_RE.fullmatch(tok)), None)

    lot: str | None = None
    wafer: str | None = None

    if insertion_idx is not None:
        if insertion_idx >= 2 and re.fullmatch(r"\d+", tokens[1]):
            lot = tokens[0]
            wafer = tokens[1]
            return lot, wafer

        before = tokens[:insertion_idx]
        numeric_before = [tok for tok in before if re.fullmatch(r"\d+", tok)]
        if numeric_before:
            wafer = numeric_before[-1]

        lot_tokens = before.copy()
        if lot_tokens and re.fullmatch(r"\d{14}", lot_tokens[0]):
            lot_tokens = lot_tokens[1:]
        if wafer and lot_tokens and lot_tokens[-1] == wafer:
            lot_tokens = lot_tokens[:-1]
        if lot_tokens:
            lot = "_".join(lot_tokens)

    if not lot and tokens:
        lot = tokens[0]
    return lot, wafer


def _parse_test_meta(test_number: int, test_name: str) -> TestMeta | None:
    name = str(test_name or "").strip()
    match = _TX_PREFIX_RE.match(name)
    if not match:
        return None

    blk = match.group("blk").upper()
    try:
        freq = int(match.group("freq"))
    except Exception:
        freq = None

    lut = None
    lut_match = _FWLU_RE.search(name)
    if lut_match:
        try:
            lut = int(lut_match.group("lut"))
        except Exception:
            lut = None

    lo_idac = None
    lo_match = _POWCO_RE.search(name)
    if lo_match:
        try:
            lo_idac = int(lo_match.group("idac"))
        except Exception:
            lo_idac = None

    pa_channel = None
    channel_match = _TX_CH_RE.search(name)
    if channel_match:
        pa_channel = f"TX{channel_match.group('ch')}"

    kind = "TXLO" if blk == "TXLO" else "TXPA"
    return TestMeta(
        test_number=int(test_number),
        test_name=name,
        kind=kind,
        frequency_ghz=freq,
        lut_value=lut,
        lo_idac=lo_idac,
        pa_channel=pa_channel,
    )


def _stdf_clean_text(value: Any) -> str | None:
    if value is None:
        return None
    text = str(value).strip()
    return text or None


def _stdf_field(record_dict: dict[str, Any], *names: str) -> Any:
    for name in names:
        if name in record_dict:
            return record_dict[name]
    return None


def _stdf_to_float(value: Any) -> float | None:
    if value is None or value == "":
        return None
    if isinstance(value, bool):
        return float(value)
    if isinstance(value, (int, float)):
        result = float(value)
        if not np.isfinite(result):
            return None
        return result
    text = str(value).strip()
    if not text or text.lower() in {"nan", "na", "none", "inf", "-inf", "+inf"}:
        return None
    try:
        result = float(text)
    except ValueError:
        try:
            result = float(text.replace(",", "."))
        except ValueError:
            return None
    if not np.isfinite(result):
        return None
    return result


def _stdf_to_int(value: Any) -> int | None:
    number = _stdf_to_float(value)
    if number is None:
        return None
    try:
        return int(number)
    except (TypeError, ValueError, OverflowError):
        return None


def _open_stdf_binary(path: Path):
    suffixes = [suffix.lower() for suffix in path.suffixes]
    if suffixes and suffixes[-1] == ".gz":
        return gzip.open(path, "rb")
    if suffixes and suffixes[-1] == ".bz2":
        return bz2.open(path, "rb")
    if suffixes and suffixes[-1] == ".xz":
        return lzma.open(path, "rb")
    return path.open("rb")


def _get_or_create_virtual_test_number(
    *,
    emitted_by_key: dict[tuple[int, str], int],
    used_ids: set[str],
    base_test_number: int,
    variant: str,
) -> int:
    key = (int(base_test_number), variant)
    existing = emitted_by_key.get(key)
    if existing is not None:
        return existing

    base = str(int(base_test_number))
    if base not in used_ids:
        used_ids.add(base)
        emitted = int(base)
    else:
        duplicate_index = 1
        while True:
            candidate = f"{base}{duplicate_index:03d}"
            if candidate not in used_ids:
                used_ids.add(candidate)
                emitted = int(candidate)
                break
            duplicate_index += 1

    emitted_by_key[key] = emitted
    return emitted


def _collect_target_power_data_from_stdf(
    stdf_path: Path,
    *,
    progress_callback: Callable[[float, str], None] | None = None,
) -> DirectPowerData:
    try:
        import pystdf.V4 as V4
        from pystdf.IO import Parser
    except ImportError as exc:
        raise RuntimeError(
            "STDF parsing requires the 'pystdf' package. Install it from the current virtual environment."
        ) from exc

    name_map: dict[int, str] = {}
    limits_map: dict[int, tuple[float | None, float | None, str]] = {}
    values_by_test: dict[int, list[float]] = {}
    emitted_by_key: dict[tuple[int, str], int] = {}
    used_ids: set[str] = set()
    active_sites: set[int | None] = set()
    warnings: list[str] = []
    n_chips = 0

    source_size = None
    try:
        if stdf_path.is_file() and not any(str(stdf_path.name).lower().endswith(suffix) for suffix in (".gz", ".bz2", ".xz")):
            source_size = max(int(stdf_path.stat().st_size), 1)
    except OSError:
        source_size = None

    with _open_stdf_binary(stdf_path) as handle:
        last_fraction_reported = -1.0

        def report_progress(force: bool = False, suffix: str = "") -> None:
            nonlocal last_fraction_reported
            if progress_callback is None or source_size is None:
                return
            try:
                fraction = max(0.0, min(1.0, float(handle.tell()) / float(source_size)))
            except Exception:
                return
            if force or fraction - last_fraction_reported >= 0.05:
                last_fraction_reported = fraction
                message = f"Streaming STDF {stdf_path.name}"
                if suffix:
                    message = f"{message} - {suffix}"
                progress_callback(fraction, message)

        class _PowerParser(Parser):
            def __init__(self, inp) -> None:
                super().__init__(recTypes=(V4.far, V4.mir, V4.wir, V4.pir, V4.ptr, V4.mpr, V4.prr), inp=inp)

            def parse_records(self, count=0):
                i = 0
                self.eof = 0
                while self.eof == 0:
                    try:
                        header = self.readHeader()
                    except Exception as exc:
                        if exc.__class__.__name__ == "EofException":
                            break
                        raise

                    self.header(header)
                    if (header.typ, header.sub) in self.recordMap:
                        rec_type = self.recordMap[(header.typ, header.sub)]
                        rec_parser = self.recordParsers[(header.typ, header.sub)]
                        try:
                            fields = rec_parser(self, header, [])
                        except Exception as exc:
                            record_name = rec_type.__class__.__name__.upper()
                            warnings.append(f"Skipped malformed {record_name} record in {stdf_path.name}: {exc}")
                            if header.len > 0:
                                self.inp.read(header.len)
                                header.len = 0
                            continue

                        if len(fields) < len(rec_type.columnNames):
                            fields += [None] * (len(rec_type.columnNames) - len(fields))
                        self.send((rec_type, fields))
                        if header.len > 0:
                            self.inp.read(header.len)
                            header.len = 0
                    else:
                        self.inp.read(header.len)

                    report_progress()
                    if count:
                        i += 1
                        if i >= count:
                            break

            def send(self, data) -> None:
                nonlocal n_chips

                rec_type, fields = data
                record_name = rec_type.__class__.__name__.upper()
                record_dict = {name: value for name, value in zip(getattr(rec_type, "fieldNames", []), fields)}

                if record_name == "PIR":
                    active_sites.add(_stdf_to_int(_stdf_field(record_dict, "SITE_NUM")))
                    return

                if record_name == "PRR":
                    active_sites.discard(_stdf_to_int(_stdf_field(record_dict, "SITE_NUM")))
                    n_chips += 1
                    return

                if record_name not in {"PTR", "MPR"}:
                    return

                test_num = _stdf_to_int(_stdf_field(record_dict, "TEST_NUM"))
                if test_num is None:
                    return

                test_name = _stdf_clean_text(_stdf_field(record_dict, "TEST_TXT", "TEST_NAM", "VECT_NAM")) or str(test_num)
                unit = _stdf_clean_text(_stdf_field(record_dict, "UNITS", "UNIT")) or ""
                low = _stdf_to_float(_stdf_field(record_dict, "LO_LIMIT", "LLM", "LOW_LIMIT"))
                high = _stdf_to_float(_stdf_field(record_dict, "HI_LIMIT", "HLM", "HIGH_LIMIT"))

                def append_value(*, virtual_test_number: int, final_test_name: str, result: float | None) -> None:
                    meta = _parse_test_meta(virtual_test_number, final_test_name)
                    if not _is_target_power_test(meta):
                        return
                    if not _is_selected_power_setting(meta):
                        return
                    if not _is_dbm_unit(unit):
                        return
                    if TEST_NUMBER_FILTER and int(virtual_test_number) not in TEST_NUMBER_FILTER:
                        return
                    if result is None:
                        return

                    name_map[virtual_test_number] = final_test_name
                    existing_low, existing_high, existing_unit = limits_map.get(virtual_test_number, (None, None, ""))
                    limits_map[virtual_test_number] = (
                        low if existing_low is None else existing_low,
                        high if existing_high is None else existing_high,
                        unit or existing_unit,
                    )
                    values_by_test.setdefault(virtual_test_number, []).append(float(result))

                if record_name == "PTR":
                    virtual_test_number = _get_or_create_virtual_test_number(
                        emitted_by_key=emitted_by_key,
                        used_ids=used_ids,
                        base_test_number=int(test_num),
                        variant="PTR",
                    )
                    append_value(
                        virtual_test_number=virtual_test_number,
                        final_test_name=test_name,
                        result=_stdf_to_float(_stdf_field(record_dict, "RESULT")),
                    )
                    return

                results_raw = _stdf_field(record_dict, "RTN_RSLT", "RESULTS")
                if isinstance(results_raw, (list, tuple)):
                    results = [_stdf_to_float(value) for value in results_raw]
                else:
                    single_result = _stdf_to_float(results_raw)
                    results = [single_result] if single_result is not None else []
                if not results:
                    return

                pin_labels_raw = _stdf_field(record_dict, "RTN_INDX", "RTN_STAT", "PMR_INDX")
                if isinstance(pin_labels_raw, (list, tuple)):
                    pin_labels = [str(item) for item in pin_labels_raw]
                else:
                    pin_labels = []

                for result_index, result in enumerate(results, start=1):
                    pin_suffix = pin_labels[result_index - 1] if result_index - 1 < len(pin_labels) and pin_labels[result_index - 1] else str(result_index)
                    expanded_name = f"{test_name}[{pin_suffix}]"
                    virtual_test_number = _get_or_create_virtual_test_number(
                        emitted_by_key=emitted_by_key,
                        used_ids=used_ids,
                        base_test_number=int(test_num),
                        variant=f"MPR:{pin_suffix}",
                    )
                    append_value(
                        virtual_test_number=virtual_test_number,
                        final_test_name=expanded_name,
                        result=result,
                    )

        report_progress(force=True, suffix="0%")
        _PowerParser(handle).parse()
        report_progress(force=True, suffix="100%")

    if active_sites:
        n_chips += len(active_sites)

    arrays_by_test = {
        int(test_number): np.asarray(values, dtype=float)
        for test_number, values in values_by_test.items()
        if values
    }
    return DirectPowerData(
        name_map=name_map,
        limits_map=limits_map,
        arrays_by_test=arrays_by_test,
        n_chips=int(n_chips),
        warnings=tuple(warnings),
    )


def _load_or_collect_target_power_data(
    stdf_path: Path,
    *,
    cache_dir: Path,
    cache_extra_key: str = "",
    progress_callback: Callable[[float, str], None] | None = None,
) -> DirectPowerData:
    signature = _direct_cache_signature(stdf_path, extra_key=cache_extra_key)
    cache_path = _direct_cache_path(cache_dir, stdf_path, extra_key=cache_extra_key)
    cached = _load_cached_direct_power_data(cache_path, signature=signature)
    if cached is not None:
        if progress_callback is not None:
            progress_callback(1.0, f"Loaded cached STDF slice for {stdf_path.name}")
        return cached

    data = _collect_target_power_data_from_stdf(stdf_path, progress_callback=progress_callback)
    _save_cached_direct_power_data(cache_path, signature=signature, data=data)
    return data


def _is_target_power_test(meta: TestMeta | None) -> bool:
    if meta is None:
        return False
    if meta.kind == "TXLO":
        return meta.lo_idac is not None
    return meta.lut_value is not None


def _is_selected_power_setting(meta: TestMeta | None) -> bool:
    if meta is None:
        return False
    if meta.kind == "TXLO":
        return meta.lo_idac == int(TARGET_TXLO_LO_IDAC)
    if meta.kind == "TXPA":
        return meta.lut_value == int(TARGET_TXPA_LUT)
    return False


def _is_dbm_unit(unit: str | None) -> bool:
    return str(unit or "").strip().upper() == "DBM"


def _cdf_xy(values: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    arr = np.asarray(values, dtype=float)
    arr = arr[np.isfinite(arr)]
    if arr.size == 0:
        return np.array([], dtype=float), np.array([], dtype=float)
    x = np.sort(arr)
    y = np.arange(1, arr.size + 1, dtype=float) / float(arr.size)
    return x, y


def _median(values: np.ndarray) -> float:
    arr = np.asarray(values, dtype=float)
    arr = arr[np.isfinite(arr)]
    if arr.size == 0:
        return float("nan")
    return float(np.median(arr))


def _filter_outliers_mad(values: np.ndarray, *, mad_multiplier: float) -> tuple[np.ndarray, int]:
    arr = np.asarray(values, dtype=float)
    arr = arr[np.isfinite(arr)]
    if arr.size < 5:
        return arr, 0
    med = float(np.median(arr))
    abs_dev = np.abs(arr - med)
    mad = float(np.median(abs_dev))
    if not np.isfinite(mad) or mad <= 0.0:
        return arr, 0
    keep = abs_dev <= float(mad_multiplier) * mad
    filtered = arr[keep]
    return filtered, int(arr.size - filtered.size)


def _read_flat_csv_metadata(path: Path) -> tuple[dict[int, str], dict[int, tuple[float | None, float | None, str]]]:
    with path.open("r", encoding="latin1", errors="ignore") as handle:
        rows = [handle.readline().rstrip("\n\r").split(";") for _ in range(5)]

    if len(rows) < 5 or not rows[0]:
        return {}, {}

    header, test_names, lows, highs, units = rows
    max_len = max(len(header), len(test_names), len(lows), len(highs), len(units))
    rows_norm = []
    for row in (header, test_names, lows, highs, units):
        rows_norm.append(row + [""] * (max_len - len(row)))
    header, test_names, lows, highs, units = rows_norm

    name_map: dict[int, str] = {}
    limits_map: dict[int, tuple[float | None, float | None, str]] = {}

    for col_id, test_name, low_text, high_text, unit in zip(header, test_names, lows, highs, units, strict=False):
        col_id = str(col_id).strip()
        if not col_id.isdigit():
            continue
        test_number = int(col_id)
        name_map[test_number] = str(test_name).strip()

        low = pd.to_numeric(low_text, errors="coerce")
        high = pd.to_numeric(high_text, errors="coerce")
        limits_map[test_number] = (
            float(low) if pd.notna(low) else None,
            float(high) if pd.notna(high) else None,
            str(unit).strip(),
        )

    return name_map, limits_map


def _read_flat_csv_chip_matrix(path: Path, *, tests_needed: Iterable[int] | None = None) -> tuple[pd.DataFrame, dict]:
    wanted: set[int] | None = None
    if tests_needed is not None:
        wanted = {int(t) for t in tests_needed}

    with path.open("r", encoding="latin1", errors="ignore") as handle:
        header = handle.readline().rstrip("\n\r").split(";")

    normalized_header = [str(col).strip() for col in header]
    usecols: list[str] | None = None
    if wanted:
        usecols = [col for col in normalized_header if col in _FLAT_META_COLS or (col.isdigit() and int(col) in wanted)]
        if len(usecols) < 2:
            usecols = None

    df = pd.read_csv(
        path,
        sep=";",
        header=0,
        skiprows=range(1, _SUMMARY_ROW_COUNT + 1),
        usecols=usecols,
        encoding="latin1",
        low_memory=False,
    )

    if df.empty:
        return pd.DataFrame(), {"error": "no_chip_rows"}

    test_cols = [col for col in df.columns if str(col).isdigit()]
    if not test_cols:
        return pd.DataFrame(), {"error": "test_cols_not_found"}

    values = df[test_cols].apply(pd.to_numeric, errors="coerce")
    values.columns = [int(col) for col in values.columns]

    chip_id = None
    if "CHIP_ID" in df.columns:
        chip_id = df["CHIP_ID"].astype(str).str.strip().replace({"": np.nan})
    if chip_id is None or chip_id.isna().all():
        parts: list[pd.Series] = []
        for col in ("LOT", "WAFER", "X", "Y", "UNIT_ID"):
            if col in df.columns:
                parts.append(df[col].astype(str).str.strip())
        if parts:
            chip_id = parts[0]
            for extra in parts[1:]:
                chip_id = chip_id + ":" + extra
        else:
            chip_id = pd.Series([str(i) for i in range(len(df))], index=df.index)

    chip_matrix = values.copy()
    chip_matrix.index = chip_id.astype(str)
    return chip_matrix, {"error": "", "n_tests": int(chip_matrix.shape[1]), "n_chips": int(chip_matrix.shape[0])}


