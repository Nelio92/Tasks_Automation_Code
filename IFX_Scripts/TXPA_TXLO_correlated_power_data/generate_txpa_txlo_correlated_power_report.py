def _prepare_input_files(
    input_folder: Path,
    *,
    work_dir: Path,
    cache_dir: Path,
    progress_callback: Callable[[float, str], None] | None = None,
) -> list[SourceFileInfo]:
    candidates: list[SourceFileInfo] = []
    extraction_dir = work_dir / "extracted_archives"
    extraction_dir.mkdir(parents=True, exist_ok=True)

    files = [path for path in sorted(input_folder.iterdir(), key=lambda item: item.name.lower()) if path.is_file()]
    if MAX_FILES is not None:
        files = files[: int(MAX_FILES)]

    total_files = max(len(files), 1)
    for index, path in enumerate(files, start=1):
        if progress_callback is not None:
            progress_callback((index - 1) / total_files, f"Preparing input file {index}/{total_files}: {path.name}")

        if path.suffix.lower() == ".csv":
            insertion = _insertion_from_filename(path)
            if insertion is None:
                continue
            lot, wafer = _parse_filename_metadata(path, insertion)
            candidates.append(
                SourceFileInfo(
                    source_path=path,
                    csv_path=path,
                    source_kind="csv",
                    insertion=insertion.insertion,
                    insertion_type=insertion.insertion_type,
                    temperature_c=insertion.temperature_c,
                    lot=lot,
                    wafer=wafer,
                    base_name=_strip_known_suffixes(path.name),
                )
            )
            continue

        if _has_any_suffix(path, _DIRECT_STDF_SUFFIXES):
            insertion = _insertion_from_filename(path)
            if insertion is None:
                continue
            if progress_callback is not None:
                file_start = (index - 1) / total_files
                file_span = 1 / total_files
                direct_power_data = _load_or_collect_target_power_data(
                    path,
                    cache_dir=cache_dir,
                    progress_callback=lambda fraction, message, file_start=file_start, file_span=file_span: progress_callback(
                        file_start + (file_span * fraction),
                        message,
                    ),
                )
            else:
                direct_power_data = _load_or_collect_target_power_data(path, cache_dir=cache_dir)
            lot, wafer = _parse_filename_metadata(path, insertion)
            candidates.append(
                SourceFileInfo(
                    source_path=path,
                    csv_path=None,
                    source_kind="stdf-direct",
                    insertion=insertion.insertion,
                    insertion_type=insertion.insertion_type,
                    temperature_c=insertion.temperature_c,
                    lot=lot,
                    wafer=wafer,
                    base_name=_strip_known_suffixes(path.name),
                    direct_power_data=direct_power_data,
                )
            )
            continue

        if _has_any_suffix(path, _TAR_STDF_SUFFIXES):
            with tempfile.TemporaryDirectory(prefix="txpa_txlo_stdf_") as temp_dir:
                extracted_members = _extract_stdf_members_from_tar(path, Path(temp_dir))
                for extracted in extracted_members:
                    insertion = _insertion_from_filename(extracted)
                    if insertion is None:
                        continue
                    if progress_callback is not None:
                        file_start = (index - 1) / total_files
                        file_span = 1 / total_files
                        direct_power_data = _load_or_collect_target_power_data(
                            extracted,
                            cache_dir=cache_dir,
                            cache_extra_key=f"{path.resolve()}::{extracted.name}",
                            progress_callback=lambda fraction, message, file_start=file_start, file_span=file_span: progress_callback(
                                file_start + (file_span * fraction),
                                message,
                            ),
                        )
                    else:
                        direct_power_data = _load_or_collect_target_power_data(
                            extracted,
                            cache_dir=cache_dir,
                            cache_extra_key=f"{path.resolve()}::{extracted.name}",
                        )
                    lot, wafer = _parse_filename_metadata(extracted, insertion)
                    candidates.append(
                        SourceFileInfo(
                            source_path=path,
                            csv_path=None,
                            source_kind="tar.gz-direct",
                            insertion=insertion.insertion,
                            insertion_type=insertion.insertion_type,
                            temperature_c=insertion.temperature_c,
                            lot=lot,
                            wafer=wafer,
                            base_name=_strip_known_suffixes(extracted.name),
                            direct_power_data=direct_power_data,
                        )
                    )

    return candidates


